uint16_t adc[4]; // ADC buf
uint16_t aadc = 0;
uint16_t mod = 500;
enum fsm_states {IDLE = 0, PRE, WAIT, TRIGD, FIN, ERR};
struct fsm_t {
  uint8_t mode; // FSM mode
} fsm;
struct shut_t s = {.trigger = 0};

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_UCPD1_Init();
  MX_USB_Device_Init();
  MX_ADC1_Init();
  MX_CORDIC_Init();
  MX_FMAC_Init();
  MX_RNG_Init();
  MX_TIM2_Init();
  MX_TIM4_Init();
  MX_TIM6_Init();
  MX_COMP2_Init();
  MX_DAC1_Init();

  // LED-TIM
  HAL_TIM_Base_Start_IT(&htim6);
  // ADC
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *) &adc[0], 4);
  HAL_TIM_Base_Start_IT(&htim4);
  // DAC
  HAL_COMP_Start(&hcomp2);
  HAL_DAC_Start(&hdac1, DAC_CHANNEL_2);

  MX_USBPD_Init();
  while (1)
  {
    switch (fsm.mode){
      case PRE:
      default:

        HAL_Delay(500);
        aadc = ((adc[0] + adc[1] + adc[2] + adc[3]) / 4) - mod;
        HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, aadc);
        s.trigger = 0;

        if(0){
          fsm.mode = ERR;
        } else {
          fsm.mode = WAIT;
        }
        break;
      case WAIT:

        aadc = ((adc[0] + adc[1] + adc[2] + adc[3]) / 4) - mod;
        HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, aadc);
        s.trigger = 0;

        if(0){
          fsm.mode = ERROR;
        } else {
          fsm.mode = TRIGD;
        }
        break;
      case TRIGD:

        if(0){
          fsm.mode = ERROR;
        } else if(s.trigger == 2){
          fsm.mode = FIN;
        }
        break;
      case FIN:

        char tg[64];
        memset(tg, '\0' , 64);
        s.elapsed_time = (uint32_t)((float)(s.elapsed+1)*(float)(1.0/168.0));
        if(s.elapsed){
          s.speed = (1000000.0/(float)(s.elapsed_time));
        } else {
          s.speed = 0;
        }
        sprintf(tg, "%lulsb,\t%luus,\t1/%lus\r\n", s.elapsed,
                                          s.elapsed_time,
                                          s.speed);
        CDC_Transmit_FS((uint8_t *) tg, 64);

        s.trigger = 0;

        if (s.trigger == 0){
          fsm.mode = PRE;
        }
        break;
      case ERR:

        if(0){
          fsm.mode = PRE;
        }
        break;
    }
  }
}

